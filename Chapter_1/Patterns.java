// 10. Binary Tree Traversal
/* Binary trees are all about traversing it in a specific order. The four popular ways to traverse the binary tree are :-
   a. Pre Order:- root->left->right
   b. In Order:- left->root->right
   c. Post Order:- left->right->root
   d. Level order: level by level

   To retrieve the values of the binary search tree in sorted order, use inorder traversal.
   To create a copy of a tree (serialization), use preorder traversal.
   When you want to process child nodes before the parent, use postorder traversal.
   When you need to explore all nodes at current level before next, use level order traversal.

   Questions :- 257,230,124,107
 */

